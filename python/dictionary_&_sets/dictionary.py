# .dictionary is a collection of key-value pairs
myDict = {
    "Fast": "In a quick manner",
    "rutik": "A Coder",
    "ruu": [1,1,2,2],
    "nested": {"pratik":"yash"}
}

print(myDict['Fast'])
print(myDict['rutik'])
print(myDict['ruu'])
print(myDict['nested']['pratik'])

