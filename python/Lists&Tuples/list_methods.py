L1 = [1,8,7,2,21,15]
print(L1)

L1.sort()     # sorts the list
L1.reverse()  # reverse the list
L1.append(10) # adds 10 at the end of the list
L1.insert(2,20) # inserts 20 at index 2
L1.pop(2) # will delect element at index 2 and return its value
L1.remove(21) #will remove 21 from list
print(L1)